using System;
using System.Globalization;

namespace CourseTypeBasic
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 10;
            Console.WriteLine(a);

            a += 2;
            Console.WriteLine(a);

            a -= 3;
            Console.WriteLine(a);

            a *= 3;
            Console.WriteLine(a);

            a /= 3;
            Console.WriteLine(a);

            a %= 3;
            Console.WriteLine(a);
        }
    }
}
